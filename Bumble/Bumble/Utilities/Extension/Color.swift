import Foundation
import SwiftUI

extension Color {
    enum Custom {
        static let black: Color = Color(.bumbleBlack)
        static let gray: Color = Color(.bumbleGray)
        static let yellow: Color = Color(.bumbleYellow)
        static let lightYellow: Color = Color(.bumbleLightYellow)
        static let background: Color = Color(.bumbleBackground)
    }
}
